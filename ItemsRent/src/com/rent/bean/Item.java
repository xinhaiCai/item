package com.rent.bean;

public class Item {
private int item_id;
private String item_name;
private String item_discription;
private int item_price;
private String item_owner;
private int item_type_id;
private String item_picture;
private String item_add_date;
private int item_num;

public int getItem_id() {
	return item_id;
}
public void setItem_id(int item_id) {
	this.item_id = item_id;
}
public String getItem_name() {
	return item_name;
}
public void setItem_name(String item_name) {
	this.item_name = item_name;
}
public String getItem_discription() {
	return item_discription;
}
public void setItem_discription(String item_discription) {
	this.item_discription = item_discription;
}
public int getItem_price() {
	return item_price;
}
public void setItem_price(int item_price) {
	this.item_price = item_price;
}
public String getItem_owner() {
	return item_owner;
}
public void setItem_owner(String item_owner) {
	this.item_owner = item_owner;
}
public int getItem_type_id() {
	return item_type_id;
}
public void setItem_type_id(int item_type_id) {
	this.item_type_id = item_type_id;
}
public String getItem_picture() {
	return item_picture;
}
public void setItem_picture(String item_picture) {
	this.item_picture = item_picture;
}
public String getItem_add_date() {
	return item_add_date;
}
public void setItem_add_date(String item_add_date) {
	this.item_add_date = item_add_date;
}
public int getItem_num() {
	return item_num;
}
public void setItem_num(int item_num) {
	this.item_num = item_num;
}
}
