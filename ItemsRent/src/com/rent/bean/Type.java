package com.rent.bean;

public class Type {
	private int type_id;
	private String type_name;
	private String type_discription;
	
	public int getType_id() {
		return type_id;
	}
	public void setType_id(int type_id) {
		this.type_id = type_id;
	}
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public String getType_discription() {
		return type_discription;
	}
	public void setType_discription(String type_discription) {
		this.type_discription = type_discription;
	}
}
