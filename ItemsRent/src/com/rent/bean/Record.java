package com.rent.bean;

public class Record {
private int record_id;
private int record_item_id;
private int record_item_num;
private int record_order_id;

public int getRecord_id() {
	return record_id;
}
public void setRecord_id(int record_id) {
	this.record_id = record_id;
}
public int getRecord_item_id() {
	return record_item_id;
}
public void setRecord_item_id(int record_item_id) {
	this.record_item_id = record_item_id;
}
public int getRecord_item_num() {
	return record_item_num;
}
public void setRecord_item_num(int record_item_num) {
	this.record_item_num = record_item_num;
}
public int getRecord_order_id() {
	return record_order_id;
}
public void setRecord_order_id(int record_order_id) {
	this.record_order_id = record_order_id;
}
}
